    private void btnAddSoftwareActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddSoftwareActionPerformed
    String softwareName = txtNameSoftware.getText();
        if (softwareName == null || softwareName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese todos los campos obligatorios.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            SoftwareDTO softwareToSave = new SoftwareDTO();
            softwareToSave.setSoftwareName(softwareName);
            this.softwareBO.saveSoftware(softwareToSave);
           
            JOptionPane.showMessageDialog(this, "Registro agregado correctamente.", "Informaci√≥n", JOptionPane.INFORMATION_MESSAGE);
            frmSoftwareManager.loadFrame();
            this.dispose();

        } catch (BusinessException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddSoftwareActionPerformed


